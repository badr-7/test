#include"libftprintf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
		i += ft_putstr("(null)");
	if (s != NULL)
	{
		while (*s)
		{
			write(1, &*s, 1);
			i++;
			s++;
		}
	}
	return (i);
}
